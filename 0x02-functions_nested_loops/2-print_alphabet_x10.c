#include "main.h"
/**
 * main- print x10 the alphabets
 * 
 * Return: Always 0 (success)
 * 
 */

void print_alphabet_x10(void);
{
    int alpha;
    int alpha2;

    for (alpha = 0; alpha <= 9; alpha++)
    {
        for (alpha = 'a'; alpha <= 'z'; alpha++)
        {
            _putchar(alpha2);
        }
        _putchar('\n');
    }
    

}